   <h1>Guanine</h1>

   <img src="https://upload.wikimedia.org/wikipedia/commons/thumb/3/3f/Guanin.svg/300px-Guanin.svg.png" alt="">

<p>
Guanine (/ˈɡwɑːnɪn/; or G, Gua) is one of the four main nucleobases found in
the nucleic acids DNA and RNA, the others being adenine, cytosine, and thymine
(uracil in RNA). In DNA, guanine is paired with cytosine. The guanine
nucleoside is called guanosine.
